/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM1_Init();
  /* USER CODE BEGIN 2 */
  Track_Sensor_State_t Sensor_State;
  uint8_t state_byte = 0;

  // 启动两个PWM通道
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1); // ENA (Left)
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2); // ENB (Right)

  // 上电延时，等待系统稳定
  Motor_Set_Speed(0, 0); // 确保电机停止
  HAL_Delay(1000);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* 1. 读取传感器状态 */
    Sensor_State = Track_Sensor_Read();

    /* 2. 组合 5 位为一个字节（左→右 = Out5..Out1） */
    state_byte = (Sensor_State.Out5 << 4) |
                 (Sensor_State.Out4 << 3) |
                 (Sensor_State.Out3 << 2) |
                 (Sensor_State.Out2 << 1) |
                 (Sensor_State.Out1);

    /* 3. 循迹逻辑（黑 = 1, 白 = 0）*/
    switch (state_byte)
    {
        /* ========= ① 直行 ========= */
        case 0b00100:  // 只有中间 Out3=1
        case 0b01110:  // 中间三路都看到黑
        case 0b11111:  // 全黑（十字路口）
            Motor_Set_Speed(BASE_SPEED, BASE_SPEED);
            break;

        /* ========= ② 左偏（右边压线）向左修正 ========= */
        case 0b10010:  // Out2 = 1
        case 0b00011:  // Out2 Out1 = 1
            Motor_Set_Speed(BASE_SPEED - TURN_SPEED_LIGHT,
                            BASE_SPEED + TURN_SPEED_LIGHT);
            break;

        /* ========= ③ 右偏（左边压线）向右修正 ========= */
        case 0b01000:  // Out4 = 1
        case 0b11000:  // Out5 Out4 = 1
            Motor_Set_Speed(BASE_SPEED + TURN_SPEED_LIGHT,
                            BASE_SPEED - TURN_SPEED_LIGHT);
            break;

        /* ========= ④ 严重左偏（最右 1）急左转 ========= */
        case 0b00001:  // Out1 = 1
            Motor_Set_Speed(0,
                            BASE_SPEED + TURN_SPEED_HARD);
            break;

        /* ========= ⑤ 严重右偏（最左 1）急右转 ========= */
        case 0b10000:  // Out5 = 1
            Motor_Set_Speed(BASE_SPEED + TURN_SPEED_HARD,
                            0);
            break;

        /* ========= ⑥ 丢线（全白） ========= */
        case 0b00000:
            Motor_Set_Speed(0, 0);
            break;

        /* ========= ⑦ 未定义情况（稳定） ========= */
        default:
            Motor_Set_Speed(0, 0);
            break;
    }

    HAL_Delay(10);
  }
  /* USER CODE END 3 */
}

